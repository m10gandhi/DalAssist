package com.grp17.dalassist.Assist;



public class commentmodel1 {

    String email;
    String username;

    public commentmodel1() {
    }

    public commentmodel1(String email, String username) {
        this.email = email;
        this.username = username;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }
}
